// [B28-B] Offline HTML export — render a full chat session into a single,
// self-contained static .html file with inline CSS and no JS / no external
// assets. Output is safe to email, paste into Notion, or archive.
//
// Wiring: this module exposes `exportOfflineHtml(sid)` and registers it on
// `window.rccExportOfflineHtml` so B28-A's export menu (ChatHeader) can call
// it without a static import cycle. B28-A owns the menu wiring; this module
// only provides the function. If B28-A switches to a direct import instead
// of the window hook, they can import `exportOfflineHtml` by name.
//
// Security: every user/assistant-supplied string is HTML-escaped before being
// placed in the document. No interpolation into attributes/URIs beyond the
// title attribute (also escaped). No <script> is ever emitted.

import type { ChatMessage, ChatSegment, SessionMeta } from "@rcc/protocol";
import { loadCachedMessages } from "../hooks/useOfflineHydrate";

// --- public API ------------------------------------------------------------

/** Source of chat + session meta. Kept narrow so tests / B28-A can pass a
 * shim instead of the real stores. */
export interface OfflineHtmlSources {
  /** Return the active message list for `sid` (already merged/hydrated). */
  getMessages: (sid: string) => readonly ChatMessage[];
  /** Return the SessionMeta for `sid`, if known. */
  getSession: (sid: string) => SessionMeta | undefined;
}

export interface ExportOfflineHtmlOptions {
  /** Override the default filename ("rcc-chat-<sid>-<date>.html"). */
  filename?: string;
  /** Skip triggering a browser download; return the string only. */
  download?: boolean;
}

export interface ExportOfflineHtmlResult {
  html: string;
  filename: string;
  bytes: number;
}

/** Pure: build the HTML string. No DOM / download side effects. Exported for
 * tests and any callers that want to serialize without downloading. */
export function buildOfflineHtml(
  session: SessionMeta | undefined,
  sid: string,
  messages: readonly ChatMessage[],
): string {
  const title = session?.title?.trim() || sid;
  const cwd = session?.cwd || "";
  const exportedAt = new Date();
  const docTitle = `RCC · ${title}`;

  const header = `
    <header class="hdr">
      <div class="hdr-title">${escapeHtml(title)}</div>
      <dl class="hdr-meta">
        <div><dt>SID</dt><dd><code>${escapeHtml(sid)}</code></dd></div>
        ${cwd ? `<div><dt>CWD</dt><dd><code>${escapeHtml(cwd)}</code></dd></div>` : ""}
        <div><dt>Exported</dt><dd>${escapeHtml(formatDateTime(exportedAt))}</dd></div>
        <div><dt>Messages</dt><dd>${messages.length}</dd></div>
      </dl>
    </header>
  `;

  const body = messages.length === 0
    ? `<div class="empty">（无消息）</div>`
    : messages.map(renderMessage).join("\n");

  return [
    "<!doctype html>",
    `<html lang="zh-CN"><head>`,
    `<meta charset="utf-8">`,
    `<meta name="viewport" content="width=device-width,initial-scale=1">`,
    `<meta name="generator" content="RCC offline export">`,
    `<title>${escapeHtml(docTitle)}</title>`,
    `<style>${OFFLINE_CSS}</style>`,
    `</head><body>`,
    `<main class="doc">`,
    header,
    `<section class="chat">`,
    body,
    `</section>`,
    `<footer class="ftr">Generated by RCC · static snapshot · ${escapeHtml(formatDate(exportedAt))}</footer>`,
    `</main>`,
    `</body></html>`,
  ].join("");
}

/** Build + download (when running in a browser). Returns the HTML string,
 * filename, and byte size — callers can use those for telemetry / toast. */
export function exportOfflineHtml(
  sid: string,
  sources: OfflineHtmlSources,
  opts: ExportOfflineHtmlOptions = {},
): ExportOfflineHtmlResult {
  const session = sources.getSession(sid);
  const messages = sources.getMessages(sid);
  const html = buildOfflineHtml(session, sid, messages);
  const filename = opts.filename ?? defaultFilename(sid);
  const bytes = byteLength(html);

  if (opts.download !== false && typeof document !== "undefined") {
    triggerDownload(html, filename);
  }

  return { html, filename, bytes };
}

/** Install `window.rccExportOfflineHtml(sid)` so ChatHeader's export menu
 * (owned by B28-A) can call it without importing this module directly.
 * Returns an unregister fn for onCleanup. */
export function registerOfflineHtmlWindowHook(sources: OfflineHtmlSources): () => void {
  if (typeof window === "undefined") return () => {};
  const prev = (window as unknown as { rccExportOfflineHtml?: unknown })
    .rccExportOfflineHtml;
  (window as unknown as Record<string, unknown>).rccExportOfflineHtml = (
    sid: string,
    opts?: ExportOfflineHtmlOptions,
  ) => exportOfflineHtml(sid, sources, opts);
  return () => {
    const cur = (window as unknown as Record<string, unknown>).rccExportOfflineHtml;
    // Only remove if still ours (guards against B28-A overwriting later).
    if (
      (window as unknown as Record<string, unknown>).rccExportOfflineHtml === cur
    ) {
      if (prev === undefined) {
        delete (window as unknown as Record<string, unknown>).rccExportOfflineHtml;
      } else {
        (window as unknown as Record<string, unknown>).rccExportOfflineHtml = prev;
      }
    }
  };
}

/** Default source adapter: reads from the localStorage offline cache (which
 * `streaming.ts` keeps in sync for the active sid) and a caller-provided
 * session list accessor. B28-A can pass this or a richer adapter. */
export function createDefaultSources(
  getSessions: () => readonly SessionMeta[],
): OfflineHtmlSources {
  return {
    getMessages: (sid) => loadCachedMessages(sid),
    getSession: (sid) => getSessions().find((s) => s.id === sid),
  };
}

// --- rendering -------------------------------------------------------------

function renderMessage(msg: ChatMessage): string {
  const role = msg.role;
  const roleLabel = role === "user" ? "你" : role === "assistant" ? "Claude" : "系统";
  const avatar = role === "assistant"
    ? `<span class="avatar avatar-ai" aria-hidden="true"></span>`
    : role === "user"
      ? `<span class="avatar avatar-user" aria-hidden="true">你</span>`
      : `<span class="avatar avatar-sys" aria-hidden="true">S</span>`;
  const ts = escapeHtml(formatDateTime(new Date(msg.timestamp)));
  const segments = msg.segments.map(renderSegment).join("");
  return `
    <article class="msg msg-${escapeAttr(role)}">
      <div class="msg-gutter">${avatar}</div>
      <div class="msg-body">
        <div class="msg-meta"><span class="role">${escapeHtml(roleLabel)}</span><span class="time">${ts}</span></div>
        <div class="msg-segments">${segments || `<div class="seg-empty">（空）</div>`}</div>
      </div>
    </article>
  `;
}

function renderSegment(seg: ChatSegment): string {
  switch (seg.kind) {
    case "text":
      return `<div class="seg seg-text">${escapeHtmlPreservingParagraphs(seg.content)}</div>`;
    case "code": {
      const lang = seg.lang ? ` data-lang="${escapeAttr(seg.lang)}"` : "";
      const langBadge = seg.lang
        ? `<div class="code-lang">${escapeHtml(seg.lang)}</div>`
        : "";
      return `<div class="seg seg-code">${langBadge}<pre><code${lang}>${escapeHtml(seg.content)}</code></pre></div>`;
    }
    case "diff": {
      const path = seg.path
        ? `<div class="diff-path">${escapeHtml(seg.path)}</div>`
        : "";
      return `<div class="seg seg-diff">${path}<pre class="diff-body">${renderDiffLines(seg.content)}</pre></div>`;
    }
    case "tool_use": {
      const out = seg.output
        ? `<details class="tool-output"><summary>输出</summary><pre>${escapeHtml(seg.output)}</pre></details>`
        : "";
      return `<div class="seg seg-tool"><div class="tool-head">🔧 <strong>${escapeHtml(seg.tool)}</strong></div><pre class="tool-input">${escapeHtml(seg.input)}</pre>${out}</div>`;
    }
    case "thinking":
      return `<details class="seg seg-thinking"><summary>思考过程</summary><div class="thinking-body">${escapeHtmlPreservingParagraphs(seg.content)}</div></details>`;
    case "tool_result": {
      const cls = seg.isError ? "seg-tool-result seg-tool-result-error" : "seg-tool-result";
      const label = seg.isError ? "工具错误" : "工具结果";
      return `<div class="seg ${cls}"><div class="tool-head">${escapeHtml(label)}</div><pre>${escapeHtml(seg.content)}</pre></div>`;
    }
    default: {
      // Exhaustiveness: if the protocol adds a segment kind, render a safe
      // fallback rather than throw.
      const unknown = seg as { kind?: unknown };
      return `<div class="seg seg-unknown">未知段: ${escapeHtml(String(unknown.kind ?? ""))}</div>`;
    }
  }
}

function renderDiffLines(content: string): string {
  return content
    .split("\n")
    .map((line) => {
      const cls =
        line.startsWith("+++") || line.startsWith("---")
          ? "diff-file"
          : line.startsWith("@@")
            ? "diff-hunk"
            : line.startsWith("+")
              ? "diff-add"
              : line.startsWith("-")
                ? "diff-del"
                : "diff-ctx";
      return `<span class="${cls}">${escapeHtml(line)}</span>`;
    })
    .join("\n");
}

// --- escaping --------------------------------------------------------------

export function escapeHtml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function escapeAttr(s: string): string {
  // Same table as escapeHtml — attribute context is stricter only for
  // unquoted attrs, which we never emit.
  return escapeHtml(s);
}

/** HTML-escape `s`, then wrap each blank-line-delimited paragraph in <p> so
 * line breaks survive round-trip without needing white-space:pre. Single
 * newlines become <br>. */
function escapeHtmlPreservingParagraphs(s: string): string {
  const escaped = escapeHtml(s);
  const paragraphs = escaped.split(/\n\s*\n/);
  return paragraphs
    .map((p) => `<p>${p.replace(/\n/g, "<br>")}</p>`)
    .join("");
}

// --- utilities -------------------------------------------------------------

function defaultFilename(sid: string): string {
  const d = new Date();
  const stamp =
    d.getFullYear().toString() +
    pad2(d.getMonth() + 1) +
    pad2(d.getDate()) +
    "-" +
    pad2(d.getHours()) +
    pad2(d.getMinutes());
  // Restrict sid to filename-safe chars.
  const safeSid = sid.replace(/[^a-zA-Z0-9._-]/g, "_").slice(0, 40) || "chat";
  return `rcc-chat-${safeSid}-${stamp}.html`;
}

function pad2(n: number): string {
  return n < 10 ? `0${n}` : String(n);
}

function formatDate(d: Date): string {
  return `${d.getFullYear()}-${pad2(d.getMonth() + 1)}-${pad2(d.getDate())}`;
}

function formatDateTime(d: Date): string {
  return `${formatDate(d)} ${pad2(d.getHours())}:${pad2(d.getMinutes())}`;
}

function byteLength(s: string): number {
  if (typeof TextEncoder !== "undefined") return new TextEncoder().encode(s).length;
  // Fallback byte-count approximation for non-browser envs.
  let n = 0;
  for (let i = 0; i < s.length; i++) {
    const c = s.charCodeAt(i);
    n += c < 0x80 ? 1 : c < 0x800 ? 2 : 3;
  }
  return n;
}

function triggerDownload(html: string, filename: string): void {
  try {
    const blob = new Blob([html], { type: "text/html;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = filename;
    a.rel = "noopener";
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    // Revoke on next tick so Safari has time to pick up the blob.
    setTimeout(() => URL.revokeObjectURL(url), 0);
  } catch {
    /* ignore — fall back to returning the html string only */
  }
}

